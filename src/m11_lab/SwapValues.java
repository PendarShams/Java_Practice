package m11_lab;

public class SwapValues {

    public static void main(String[] args) {

        int num1 = 5;
        int num2 = 10;
        int temp;

        temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);





    }
}
